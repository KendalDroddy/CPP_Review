#include <iostream>

void smallSort(int *a, int *b, int *c);

/*********************************************************************
**                       MAIN
*********************************************************************/
#include <iostream>

int main()
{
  int a = 34;
  int b = 9;
  int c = -4;

  smallSort(&a, &b, &c);

  std::cout << a << ", " << b << ", " << c << std::endl;
}

/*********************************************************************
**                       smallSort()
*********************************************************************/
void smallSort(int *a, int *b, int *c)
{
  if(*a > *b)
  {
    int temp = *a;
    *a = *b;
    *b = temp;
  }
  if(*a > *c)
  {
    int temp = *a;
    *a = *c;
    *c = temp;
  }
  if(*b > *c)
  {
    int temp = *b;
    *b = *c;
    *c = temp;
  }
}